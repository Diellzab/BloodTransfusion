package com.fiek.transfuzioni_gjakut.models;

public class RarestHelperClass {

    int img;
    String title;

    public RarestHelperClass(int img, String title) {
        this.img = img;
        this.title = title;
    }

    public int getImg() {
        return img;
    }

    public String getTitle() {
        return title;
    }
}
