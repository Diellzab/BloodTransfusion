package com.fiek.transfuzioni_gjakut.activities;

public class AddUserClass {

    private String maleFemale;
    private String emri;
    private String mbiemri;
    private String email;
    private String password;
    private String tipiGjakut;
    private String telefoni;
    private String adresa;

    public AddUserClass() {
    }

    public String getMaleFemale() {
        return maleFemale;
    }

    public void setMaleFemale(String maleFemale) {
        this.maleFemale = maleFemale;
    }

    public String getEmri() {
        return emri;
    }

    public void setEmri(String emri) {
        this.emri = emri;
    }

    public String getMbiemri() {
        return mbiemri;
    }

    public void setMbiemri(String mbiemri) {
        this.mbiemri = mbiemri;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getTipiGjakut() {
        return tipiGjakut;
    }

    public void setTipiGjakut(String tipiGjakut) {
        this.tipiGjakut = tipiGjakut;
    }

    public String getTelefoni() {
        return telefoni;
    }

    public void setTelefoni(String telefoni) {
        this.telefoni = telefoni;
    }

    public String getAdresa() {
        return adresa;
    }

    public void setAdresa(String adresa) {
        this.adresa = adresa;
    }
}
